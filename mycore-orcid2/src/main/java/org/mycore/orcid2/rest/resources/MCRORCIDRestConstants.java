package org.mycore.orcid2.rest.resources;

/**
 * This class holds constants used in REST end points for ORCID-related operations.
 */
public class MCRORCIDRestConstants {

    /**
     * The name of the path parameter used to specify an ORCID iD in URL paths.
     */
    public static final String PATH_PARAM_ORCID = "orcid";

    /**
     * The name of the path parameter used to specify an object Id in URL paths.
     */
    public static final String PATH_PARAM_OBJECT_ID = "objectId";

    /**
     * The name of the query parameter used to specify an ORCID iD in URL query strings.
     */
    public static final String QUERY_PARAM_ORCID = "orcid";

    /**
     * The name of the query parameter used to specify an object Id in URL query strings.
     */
    public static final String QUERY_PARAM_OBJECT_ID = "objectId";

}
